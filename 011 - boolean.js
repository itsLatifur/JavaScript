// Declare a variable 'b1' and assign a boolean value 'true'
var b1 = true; // Represents a true boolean value

// Declare a variable 'b2' and assign a boolean value 'false'
var b2 = false; // Represents a false boolean value

// Declare a variable 'b3' and assign a boolean value using the Boolean constructor
var b3 = Boolean(true); // Converts the value 'true' to a boolean (already true)

// Declare a variable 'b4' and assign a boolean value using the Boolean constructor
var b4 = Boolean(false); // Converts the value 'false' to a boolean (already false)

// Declare a variable 'b5' and assign a boolean value using the Boolean constructor with a number
var b5 = Boolean(1); // Converts the number 1 to a boolean (true)

// Declare a variable 'b6' and assign a boolean value using the Boolean constructor with a number
var b6 = Boolean(0); // Converts the number 0 to a boolean (false)

// Log all the boolean variables to the console
console.log(b1, b2, b3, b4, b5, b6); 
// Output: true false true false true false