// Primitive data types in JavaScript

// Number: Represents numeric values, including integers and floating-point numbers
// Examples:
Number // e.g. 10, 4.5, 8.12, 3.14

// String: Represents text enclosed in single or double quotes
// Examples:
String // 'this is a text' + "This is also a text"

// Boolean: Represents true or false values
Boolean // true or false

// undefined: Represents a variable that has been declared but not assigned a value
undefined // Example: let x; console.log(x); // Output: undefined

// null: Represents an intentional absence of any value
null // Example: let y = null; console.log(y); // Output: null

// Object data types in JavaScript

// Object: Represents a collection of key-value pairs
// Example: { name: "Alice", age: 25 }

// Array: Represents a list of values, which can be of any data type
// Example: [1, 2, 3, "text", true]
array