// Declare a variable 'str' and assign a string value using single quotes
var str = 'String'; // Represents a string value

// Declare a variable 'str2' and assign a string value using double quotes
var str2 = "String"; // Represents a string value

// Declare a variable 'str3' and assign a string value using template literals
var str3 = `String`; // Represents a string value

// Declare a variable 'str4' and assign a string value using the String constructor
var str4 = String('ssffdd'); // Converts the value 'ssffdd' to a string

// Declare a variable 'str5' and assign a numeric value converted to a string
var str5 = String(156); // Converts the number 156 to a string

// Declare a variable 'str6' and assign a floating-point value converted to a string
var str6 = String(3.1416); // Converts the number 3.1416 to a string

// Log all the string variables to the console
console.log(str, str2, str3, str4, str5, str6); 
// Output: String String String ssffdd 156 3.1416