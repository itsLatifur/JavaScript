// Log a simple string to the console
console.log('Hello World'); // Output: Hello World

// Log another string to the console
console.log("I love Bangladesh"); // Output: I love Bangladesh

// Log a number to the console
console.log(45); // Output: 45

// Log a floating-point number to the console
console.log(60.789); // Output: 60.789

// Concatenate a string with a number and log it
console.log('My Fav No: ' + 9); // Output: My Fav No: 9

// Add two numbers and log the result
console.log(9 + 9); // Output: 18

// Concatenate a string and a number (number is converted to a string)
console.log('9' + 9); // Output: 99