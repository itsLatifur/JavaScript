// Demonstrating different numeric representations in JavaScript

var hex = 0xFF; // Hexadecimal representation of 255, must use 0x as prefix to indicate hex
console.log(hex); // Output: 255 in decimal

var octal = 0o377; // Octal representation of 255, must use 0o as prefix to indicate octal
console.log(octal); // Output: 255 in decimal

var binary = 0b11111111; // Binary representation of 255, must use 0b as prefix to indicate binary
console.log(binary); // Output: 255 in decimal