// Declare a variable 'abc' without assigning a value
var abc; // 'abc' is declared but not initialized, so its value is 'undefined'

// Declare a variable 'xyz' and explicitly assign a null value
var xyz = null; // 'xyz' is explicitly set to 'null', representing no value or an empty value

// Log the value of 'abc' to the console
console.log(abc); // Output: undefined (default value for uninitialized variables)

// Log the value of 'xyz' to the console
console.log(xyz); // Output: null (explicitly assigned null value)