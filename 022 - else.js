/****** Examplea of Else ********/


// Comparing two numbers a and b
var a = 10;
var b = 20;

if (a > b) {
  console.log("a is greater than b"); // Executes if a is greater than b
} else if (a < b) {
  console.log("a is less than b"); // Executes if a is less than b
}

// Checking if a number n is even or odd
var n = 50;

if (n % 2 == 0) {
  console.log(n + " is even"); // Executes if n is divisible by 2
} else {
  console.log(n + " is odd"); // Executes if n is not divisible by 2
}
